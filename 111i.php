IHIBALO SA PROKYURMENT </br>
ANO ANG PROKYURMENT? </br>
</br>
Ang prokyurment amo ang pagbakal sang mga bagay o kagamitan, serbisyo kag mga pagkontrata sang mga proyekto pang-inprastraktura sang is aka opisina sang gobyerno. Ang prokyurment nga bagay nasandig sa probisyon sang aton layi sa idalom sang Republic Act 9184. 
May tatlo ka klase sang ginatawag nga prokyurment sa gobyerno. Amo ini ang mga masunod: (1) Kagamitan – amo ini ang pagbakal sang mga gamit kaangay sang papel, kag mga materyales; (2) Proyekto – in amo ang nahanungod sa mga proyektong pang-inprastraktura kaangay sang dalan, taytay, eskwelahan, ospital kag mga edipisyo pang-gobyerno. (3) Serbisyo – ini amo ang pagbayad sa mga serbisyo kaangay sang mga eksperto nga enhinyero, sayantist, kompyuter programmer kag iban pa.
SIN-O BALA ANG MGA TAWO SA SINI NGA PROSESO? 
</br>
</br>
1.  Ang manogbakal nga opisina sang gobyerno, kaangay sang is aka Komisyon, Opisina, Ahensya, Departamento, Korporasyon, Pinansyal nga Institusyon, Eskwelahan kag Unibersidad, kag mga Lokal nga Gobyerno  nga nagabakal sang mga kagamitan, proyekto kag mga serbisyo.</br>
2. Ang pinuno sang opisina sang gobyerno, kaangay sang Presidente, Direktor ukon Lider sini, kag mga lideres sang local nga gobyerno kaangay sang gobernador, mayor ukon kapitan sang is aka barangay.</br>
3. Ang Lokal nga Komitiba nga ginatawag “Bids and Awards Committee” ukon BAC.  Amo ini ang lawas sang gobyerno sa is aka Opisina nga ginatulinan sa pag-prepara, pag-proseso, kag pag-aproba sang mga bagay, proyekto okun serbbisyo nga kinahanglan baklon o bayaran sang is aka opisina sang gobyerno suno sa layi nahanugod sa pagbakal sang mga bagay sa gobyerno.</br>
4. Ang nga-Obserba sa Proseso. Ginamando sang layi nga sa tanan nga bahin sang proseso ang BAC dapat maga-imbitar sang representante san Komisyon sa Pag-Awdit (ukon opisina sang COA)kag sa duha pa ka pribado nga representante halin sa kilala nga grupo, pribado nga organisayon, sector okun sa organisasyon nga indi iya sang gobyerno (kaangay sang isa ka NGO) basta ini sila mapamatud-an nga wala direkta okun indirekta man nga interes nahanungod sa bagay, proyekto ukon serbisyo nga pagabaklon okun pagabaydan sang nahanungod nga opisina sang gobyerno.       </br>
</br>
ANG PROSESO SANG PROKYURMENT SA GOBYERNO </br>
Una: Komperensya antes ang prokyurment. Ang komitiba nga ginatawag BAC dapat anay usisaon kon handa gid ang ila nga opisina nga maga-prokyur sang bagay, proyekto okun serbisyo.</br>
Ikaduha:  Pahibalo nahanugod sa lista kag iskedyul sang prokyurment paagi sa:  (1) pag-adbertays sa mga newspaper, (2) pag butang sang notisya sa websayt (paagi sa Internet) sang is aka opisina, (3) pagpadala kag paganunsyo sang notisya sa opisia sang gobyerno nga PhilGEPS paagi sa Internet, kag (4) pag-palapnag sang mga pahibalo sa mga pang-publiko nga lugar</br>
